	def apply(i: Int) = i match {
		case 0 => x
		case 1 => y
		case 2 => z
		case 3 => w
		case _ => throw new IndexOutOfBoundsException("[Vec4](apply(int)): " + i.toString + " not in range [0, 3]")
	}

	def dot(other: Vec4) = x * other.x + y * other.y + z * other.z + w * other.w
	def length = sqrt(x * x + y * y + z * z + w * w)
	def normalize = this / length

	def unary_- = Vec4(-x, -y, -z, -w)
	def *(other: Vec4) = Vec4(x * other.x, y * other.y, z * other.z, w * other.w)
	def /(other: Vec4) = Vec4(x / other.x, y / other.y, z / other.z, w / other.w)
	def +(other: Vec4) = Vec4(x + other.x, y + other.y, z + other.z, w + other.w)
	def -(other: Vec4) = Vec4(x - other.x, y - other.y, z - other.z, w - other.w)
	def *(scalar: Float) = Vec4(x * scalar, y * scalar, z * scalar, w * scalar)
	def /(scalar: Float) = Vec4(x / scalar, y / scalar, z / scalar, w / scalar)
	def +(scalar: Float) = Vec4(x + scalar, y + scalar, z + scalar, w + scalar)
	def -(scalar: Float) = Vec4(x - scalar, y - scalar, z - scalar, w - scalar)

	def <(other: Vec4) =
		x < other.x ||
			y < other.y ||
			z < other.z ||
			w < other.w

	def <=(other: Vec4) =
		x <= other.x ||
			y <= other.y ||
			z <= other.z ||
			w <= other.w

	def >(other: Vec4) =
		x > other.x ||
			y > other.y ||
			z > other.z ||
			w > other.w

	def >=(other: Vec4) =
		x >= other.x ||
			y >= other.y ||
			z >= other.z ||
			w >= other.w

