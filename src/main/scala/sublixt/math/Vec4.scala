package sublixt.math

object Vec4 {
	val zero = Vec4(0, 0, 0, 0)
	def apply(): Vec4 = zero
	def apply(vec: Vec2, z: Float, w: Float): Vec4 = Vec4(vec.x, vec.y, z, w)
	def apply(x: Float, vec: Vec2, w: Float): Vec4 = Vec4(x, vec.x, vec.y, w)
	def apply(x: Float, y: Float, vec: Vec2): Vec4 = Vec4(x, y, vec.x, vec.y)
	def apply(vec: Vec3, w: Float): Vec4 = Vec4(vec.x, vec.y, vec.z, w)
	def apply(x: Float, vec: Vec3): Vec4 = Vec4(x, vec.x, vec.y, vec.z)
}

